;; La etapa 1 cargo este codigo en 0x0000:0x7E00 y salto aqui. Esta etapa
;; dibuja el ESCUDO de la UTEM en arte ASCII de alta densidad usando medios
;; bloques CP437 (0xDF mitad superior, 0xDC mitad inferior, 0xDB bloque
;; completo). Los medios bloques DUPLICAN la resolucion vertical efectiva,
;; permitiendo aproximar el contorno curvo del escudo y el cuadro verde.
;;
;; El escudo se rasterizo desde el logo oficial: cada celda lleva su propio
;; par [caracter][atributo de color], por eso se usa un motor que lee pares.
;; El texto "UTEM" se dibuja con el motor simple de cadenas monocolor.
;; =============================================================================

BITS 16
ORG  0x7E00

ESC_FILA   equ 1          ; fila inicial del escudo en pantalla
ESC_COL    equ 8          ; columna inicial del escudo
ESC_NFILAS equ 19        ; numero de filas del escudo

stage2_start:
    mov ax, 0xB800
    mov es, ax            ; ES -> framebuffer VGA 0xB8000

    ; Limpiar pantalla (2000 celdas, espacio gris)
    xor di, di
    mov cx, 2000
    mov ax, 0x0720
    rep stosw

    ; -------------------------------------------------------------------------
    ; MOTOR 1: dibujar el escudo (datos = pares caracter,atributo por celda;
    ; centinela 0x00 al final de cada fila). Recorre la tabla de punteros.
    ; -------------------------------------------------------------------------
    xor bp, bp            ; BP = indice de fila del escudo (0..ESC_NFILAS-1)
.esc_fila:
    cmp bp, ESC_NFILAS
    jae .texto
    ; offset destino = ((ESC_FILA+bp)*80 + ESC_COL) * 2
    mov ax, ESC_FILA
    add ax, bp
    mov cx, 80
    mul cx
    add ax, ESC_COL
    shl ax, 1
    mov di, ax
    ; puntero a la fila: esc_rows[bp]
    mov bx, bp
    shl bx, 1
    mov si, [esc_rows + bx]
.esc_cel:
    mov al, [si]          ; caracter
    cmp al, 0x00          ; centinela fin de fila
    je .esc_next
    mov ah, [si+1]        ; atributo
    mov [es:di], ax       ; escribir celda
    add si, 2
    add di, 2
    jmp .esc_cel
.esc_next:
    inc bp
    jmp .esc_fila

    ; -------------------------------------------------------------------------
    ; MOTOR 2: texto monocolor desde tabla de descriptores
    ; (db fila, columna, color + dw puntero ; centinela fila 0xFF)
    ; -------------------------------------------------------------------------
.texto:
    mov si, tabla
.t_fila:
    mov al, [si]
    cmp al, 0xFF
    je halt_loop
    mov ah, 0
    mov cx, 80
    mul cx
    mov bl, [si+1]
    mov bh, 0
    add ax, bx
    shl ax, 1
    mov di, ax
    mov dl, [si+2]
    mov bx, [si+3]
.t_car:
    mov al, [bx]
    cmp al, 0
    je .t_next
    mov ah, dl
    mov [es:di], ax
    inc bx
    add di, 2
    jmp .t_car
.t_next:
    add si, 5
    jmp .t_fila

halt_loop:
    cli
    hlt
    jmp halt_loop

;; =============================================================================
